def square_area(side):
   area = side * side
   return area

def square_perimeter(a):
   total = a + a + a + a
   return total

def circle_area(r):
   circ = r * r * 3.14
   return circ

def circle_circumference(x):
   circum = 2 * ( x * 3.14)
   return circum

def rectangle_perimeter(l,w):
   peri = l + w + l + w
   return peri
