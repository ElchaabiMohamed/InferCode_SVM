#!/bin/usr/env python 

def square(n):
  sq = n * n
  return sq

def perimeter(n):
  per = n + n + n + n 
  return per
  
def circle_area(n):
  circ = 3.14 * (n * n)
  return circ
 
def circle_circumference(n):
  circle = 2 * 3.14 * n
  return circle 
  
def rectangle_perimeter(n , m):
  rec = n + m + n + m 
  return rec 
  