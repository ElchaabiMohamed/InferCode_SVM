#!/usr/bin/env python

def square_area(n):
   n = n * n
   return n

def square_perimeter(a):
   a = a * 4
   return a

def circle_area(b):
   b = b * b * 3.14
   return b

def circle_circumference(c):
   c = 2 * 3.14 * c
   return c

def rectangle_perimeter(d, e):
   d = d + d
   e = e + e
   return d + e


   
