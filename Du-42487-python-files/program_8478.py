#!/bin/env python
def square_area(n):
   sq = n * n
   return sq 

def square_perimeter(n):
   perimeter = n + n  
   return perimeter 


def circle_area(n):
   circle = n * n 
   return circle

def circle_circumference(n):
   circumference = n * n 
   return circumference

def rectangle_perimeter(n):
   rectangle = n * n 
   return retangle
