#!/usr/bin/env python 

def square_area(n):
   side = n ** 2
   return side 

def square_perimeter(n):
   sp = n * 4
   return sp

def circle_area(r):
   ca = r * r * 3.14
   return ca

def circle_circumference(r):
   cc = 2 * 3.14 * r
   return cc
