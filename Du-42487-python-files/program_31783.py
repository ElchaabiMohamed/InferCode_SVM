#!/usr/bin/env python

def square_area(n):
  sq = n * n
  return sq

def square_perimeter(n):
  sqper = n * 4
  return sqper

def circle_area(n):
  carea = 3.14 * (n ** 2)
  return carea

def circle_circumference(n):
  circum = 2 * 3.14 * n
  return circum

def rectangle_perimeter(n,m):
  rec = n * 2 + m * 2
  return rec
  
