def square_area(n):
    result = n * n
    return result

def square_perimeter(n):
    result = 4 * n
    return result

def circle_area(n):
    result = 3.14 * (n + n)
    return result

def circle_circumference(n):
    result = 2 * 3.141 * n
    return result

def rectangle_perimeter(n,z):
    result = (n + z) * 2
    return result


