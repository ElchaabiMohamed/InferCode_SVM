#!/usr/bin/env python

import math

def square_area(n):
   result = n * n
   return result

def square_perimeter(n):
   result = n * 4
   return result

PI = 3.14159

def circle_area(n):
   result = PI * n * n
   return result

def circle_circumference(n):
   result = 2 * PI * n
   return result

def rectangle_perimeter(length, width):
    result = 2*(length+width)
    return result

