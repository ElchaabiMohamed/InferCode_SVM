def square_area(n):
   result = n * n
   return result

def square_perimeter(n):
   result = n + n + n + n
   return result 

def circle_area(n):
   result = 3.14 * (n * n)
   return result

def circle_circumference(n):
   result = 2 * 3.14 * n

def rectangle_perimeter(a, b):
   result = a + b + a + b
   return result
