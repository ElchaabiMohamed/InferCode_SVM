def square_perimeter(n):
   result = n * 4
   return result

def rectangle_perimeter(a, b ):
   result = a * 2 + b * 2
   return result


def circle_area(n):
   result = 3.14 * n ** 2
   return result

def circle_circumference(n):
   result = 2 * 3.14 * n
   return result

