def square_area(side):
    sq = side * side
    return sq

def square_perimeter(side):
    perimeter = side * 4
    return perimeter
    
def circle_area(r):
    cir = 3.14 * (r * r)
    return cir

def circle_circumference(r):
    circum = 2 * 3.14 * r
    return circum
    
def rectangle_perimeter(a,b):
    per = (a * 2) + (b * 2)
    return per