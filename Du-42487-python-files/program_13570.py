def square_area(side):
   result = side * side
   return result

def square_perimeter(side):
   result = side + side
   return result

def circle_area(side):
   result = 3.14 * side ** 2
   return result

def circle_circumference(side):
   result = 2 * 3.14 * side
   return result


def rectangle_perimeter(sideA, sideB):
   result = (sideA * 2) + (sideB * 2)
   return result
