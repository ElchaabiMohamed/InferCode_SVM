#!/usr/bin/env python


def square_area(n):
    sq = n * n
    return sq



def square_perimeter(a):
    sp = a * 4
    return sp



def circle_area(b):
    ca = (b * 3.14) * 4
    return ca


def circle_circumference(c):
    cc = (c * 3.14) * 2
    return cc


def rectangle_perimeter(d,e):
    rp = (d * 2) + (e * 2)
    return rp
