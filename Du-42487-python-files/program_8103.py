def square_area(side):
   result = side * side
   return result

def square_perimeter(side):
   result = side * 4
   return result


def circle_area(radius):
   result = 3.14 * (radius * radius)
   return result


def circle_circumference(radius):
   result = 3.14 * (radius * 2)
   return result


def rectangle_perimeter(l,h):
   result = (l * 2) + (h * h)
   return result
