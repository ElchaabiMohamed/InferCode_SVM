#!/bin/usr/env python 

def square(n):
  sq = n * n
  return sq

def perimeter(n):
  per = n * 4
  return per
  
def circle_area(n):
  circ = 3.14 * (n * n)
  return circ
 
def circle_circumference(n):
  circle = 2 * 3.14 * n
  return circle 
  
def rectangle_perimeter(n , m):
  rec = (n * 2) + (m * 2)
  return rec 
  