def square_area(n):
   result = n * n
   return result

def square_perimeter(n):
    result = 4 * n
    return result

def circle_area(n):
   result = 3.14 * n ** 2
   return result

def circle_circumference(n):
   result = 2 * 3.14 * n
   return result

def rectangle_perimeter(a,b):
   result = 2 * a * 2 * b
   return result
