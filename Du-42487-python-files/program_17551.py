def square_area(n):
   result = n * n
   return result

def square_perimeter(n):
    result = 2 * (n + n)
    return result

def circle_area(n):
    result = n * n * 3.14
    return result

def circle_circumfrence(n):
    result = 2 * 3.14 * n
    return result

def rectangle_perimeter(a,b):
    result = 2 * ( a + b)
    return result

