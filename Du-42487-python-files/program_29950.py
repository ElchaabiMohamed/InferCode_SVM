#!/usr/bin/env python
def square_area(n):
   sq = n * n   
   return sq

def square_perimeter(p):
   sq_p = 4 * p 
   return sq_p

def circle_area(r):
   ca = r * r *3.14
   return ca

def circle_circumference(r):
   cc = 2 * 3.14 * r
   return cc

def rectangle_perimeter(a, b):
   rp = 2*a+2*b
   return rp
