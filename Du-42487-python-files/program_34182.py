def square_area(n):
   sq = n * n
   return sq
def square_perimeter(a, b):
   sqp = (a * 2) + (b * 2)
   return sqp
def circle_area(r):
   cra = (3.14 * r)**2
   return cra
def circle_circumference(r):
   cc = 2(3.14 * r)
   return cc 
def rectangle_perimeter(a, b):
   rp = (a * 2) + (b * 2)
   return rp

  

   
  
