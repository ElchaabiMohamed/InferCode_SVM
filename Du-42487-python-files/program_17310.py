def square_area(n):
	side = n * n
	return side

def square_perimeter(j):
	per = j * 4
	return per

def circle_area(i):
	area = 3.14 * (i*i)
	return area

def circle_circumference(m):
	circumference = 2 * 3.14 * m
	return circumference

def rectangle_perimeter(a,b):
	rper = (2 * a) + (2 * b)
	return rper
