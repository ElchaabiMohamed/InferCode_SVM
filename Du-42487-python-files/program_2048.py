def square_area(side):
   return side * side

def square_perimeter(side):
   return 4*side

def circle_area(side):

   return side*side * 3.14

def circle_circumference(side):
   return 2 * side * 3.14

def rectangle_perimeter(side,side2):
   return 2 * side + 2 * side2









