#!/usr/bin/env python

def square_area(n):
    n = n * n
    return n

def circle_area(b):
    b = b * b * 3.14
    return b

def square_perimeter(a):
    a = 4 * a
    return a

def circle_circumference(c):
    c = 2 * 3.14 * c
    return c

def rectangle_perimeter(l,w):
    l = l + l
    w = w + w
    return l + w



