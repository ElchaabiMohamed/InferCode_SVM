#!usr/bin/env python

def square_area(n):
    area = n * n
    return area

def square_perimeter(a):
    per = a * 4
    return per

def circle_area(r):
    circlearea = r * r 
    carea = 3.14 * circlearea
    return carea 

def circle_circumference(b):
    cir = 2 * 3.14 * b
    return cir

def rectangle_perimeter(x,y):
    side1 = x * 2
    side2 = y * 2
    rper = side1 + side2
    return rper
