def square_area(n):
   square = n * n
   return square


def square_perimeter(n):
   square = n * 4
   return square

def circle_area(n):
   square = 3.14 * n ** 2 
   return square

def circle_circumference(n):
   square =  2 * (3.14 * n) 
   return square

def rectangle_perimeter(n,m):
   square = n + n + m + m
   return square


