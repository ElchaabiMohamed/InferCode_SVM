def square_area(side):
   x = side * side
   return x

def square_perimeter(side):
   x = side + side + side + side
   return x

def circle_area(radius):
   x = 3.14 * (radius * radius)
   return x

def circle_circumference(radius):
   x = 2 * 3.14 * radius
   return x

def rectangle_perimeter(l,b): 
   x = l*2 + b*2
   return x

