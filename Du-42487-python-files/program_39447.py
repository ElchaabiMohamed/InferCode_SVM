def square_area(n):
   sq_ar = n * n
   return sq_ar

def square_perimeter(n):
   sq_pm = n * 4
   return sq_pm

def circle_area(n):
   cr_ar = 3.14 * (n * n)
   return cr_ar

def circle_circumference(n):
   cr_ci = 2 * 3.14 * n
   return cr_ci
def rectangle_perimeter(n, m):
   re_pm = sq_pm + m
   return re_pm
