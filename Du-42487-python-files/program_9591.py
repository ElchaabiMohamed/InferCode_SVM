def square_area(a):
   return a * a 

if __name__ == "__main__":
   print(square_area(2))

def square_perimeter(a):
   return a + a + a + a

if __name__ == "__main__":
   print(square_perimeter(2))

def circle_area(a):
   return a * a * 3.14

if __name__ == "__main__":
   print(circle_area(2))

def circle_circumference(a):
   return 2 * 3.14 * a

if __name__ == "__main__":
   print(circle_circumference(2))

def rectangle_perimeter(a, b):
   return a + a + b + b

if __name__ == "__main__":
   print(rectangle_perimeter(2, 2))
