def square_area(n):
    result = n * n
    return result

def square_perimeter(n):
    result = n * 4
    return result

def circle_area(n):
    result = (n * n) * 3.141
    return result

def circle_circumference(n):
    result = 2 * 3.141 * 3
    return result

def rectangle_perimeter(n):
    result = n * 4
    return result


