
def square_area(side):
	result = side * side
	return result

def square_perimeter(side):
	result = side * 4
	return result

def circle_area(radius):
	result = radius * 3.14 * 2
	return result

def circle_circumference(diameter):
	result = diameter * 3.14 * 2
	return result

def rectangle_perimeter(length , height):
	result = length + length + height + height
	return result


