#!/usr/bin/env python 

def square_area(n):
   side = n ** 2
   return side 

def square_perimeter(n):
   sp = n * 4
   return sp

def circle_area(n):
   ca = n * n * 3.14
   return ca

def circle_circumference(n):
   cc = 2 * 3.14 * n
   return cc

def rectangle_perimeter(n):
   rp = sp * 2
   return rp
