def square_area(n):
   side = n * n
   return side

def square_perimeter(n):
   perimeter = n + n + n + n
   return perimeter

def circle_area(n):
   area = 3.14 * (n*n)
   return area

def circle_circumference(n):
   circumference = 2 * 3.14 * n
   return circumference

def rectangle_perimeter(m,n):
   perimeter = m + m + n + n
   return perimeter
