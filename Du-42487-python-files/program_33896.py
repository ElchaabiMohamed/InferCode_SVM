def square_area(n):
   result = n * n
   return result

def square_perimeter(n):
    result = n * 4
    return result

def circle_area(n):
    result = 3.14 * n ** 2
    return result

def circle_circumference(n):
    result = 2 * 3.14 * n
    return result

def rectangle_perimeter(n,m):
    result = m * 2 + n * 2
    return result

