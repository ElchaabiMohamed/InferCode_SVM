#!/usr/bin/env python

def square_area(n):
	sq = n * n
	return sq

def square_perimeter(n):
	per = n * 4
	return per

def circle_area(n):
	ar = 3.14 * (n **2)
	return ar

def circle_circumference(n):
	cir = 2 * 3.14 * n
	return cir

def rectangle_perimeter(n, m):
	recper = (n * 2) + (m * 2)
	return recper