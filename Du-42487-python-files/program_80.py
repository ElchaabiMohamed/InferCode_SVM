def square_area(n):
   side = n * n
   return side

def square_perimeter(j):
	perimeter = j + j + j + j
	return perimeter

def circle_area(k):
	area= 3.14 *(k*k)
	return area

def circle_circumference(m):
	circumference= 2 * 3.14 * m
	return circumference

def rectangle_perimeter(l,h):
	perimeter= l + l + h + h
	return perimeter

