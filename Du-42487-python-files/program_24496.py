def square_area(n):
   result  = n * n
   return result 

def square_perimeter(n):
   result = n * 4
   return result 

def circle_area(n):
   result  = 3.14 * (n * n)
   return result 

def circle_circumference(n):
   result  = 2 * 3.14 * n
   return result 

def rectangle_perimeter(x,y):
   result  = 2 * x + 2 * y
   return result 
