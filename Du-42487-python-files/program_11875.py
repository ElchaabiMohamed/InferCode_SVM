
def square_perimeter(a):
   sqp = (a * 4)
   return sqp
def circle_area(r):
   cra = (r * r) * 3.14
   return cra
def circle_circumference(r):
   cc = 2(3.14 * r)
   return cc 
def rectangle_perimeter(a, b):
   rp = (a * 2) + (b * 2)
   return rp
def square_area(n):
   sq = n * n
   return sq
  

   
  
