def square_area(n):
   sq = n * n
   return square_area
def square_perimeter(n):
   sq = n + n + n + n
   return square_perimeter
def circle_area(n):
	cr = (3.14 * n) ** 2
	return circle_area
def circle_circumference(n):
	cr = 2 * 3.14 * n
	return circle_circumference
def rectangle_perimeter(y):
	rc = n + n + y + y
	return rectangle_perimeter