def square_area(n):
   result = n * n
   return result

def square_perimeter(n):
   sqperim = 2 * (n + n)
   return sqperim

def circle_area(n):
   cirarea = 3.14 * n ** 2
   return cirarea

def circle_circumference(n):
   circum = 2 * 3.14 * n
   return circum

def rectangle_perimeter(n,m):
   recperim = 2 * (n + m)
   return recperim