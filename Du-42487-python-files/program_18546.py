def square_area(side):
   result = side * side
   return result

def square_perimeter(side):
   result = side * 4
   return result

def circle_area(radius):
    result = radius * 3.14
    return result

def circle_circumference(radius):
    result = 2 * 3.14 * radius
    return result

def rectangle_perimeter(lenght, height):
    result = (lenght * 2 )+ (height * 2)
    return result 
